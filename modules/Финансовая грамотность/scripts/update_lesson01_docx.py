def delete_paragraph(paragraph: Paragraph) -> None:
    element = paragraph._element
    element.getparent().remove(element)
    paragraph._p = paragraph._element = None


def insert_after(paragraph: Paragraph, text: str, style=None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style is not None:
        new_para.style = style
    new_para.add_run(text)
    return new_para


def main() -> None:
    doc = Document(SRC)

    start = end = None
    for i, paragraph in enumerate(doc.paragraphs):
        text = paragraph.text.strip()
        if start is None and text.startswith("Откройте Canva"):
            start = i
        if start is not None and text.startswith("Сохраните сайт в Canva"):
            end = i
            break

    if start is None or end is None:
        raise RuntimeError(f"Practice block not found: start={start}, end={end}")

    anchor = doc.paragraphs[start - 1]
    list_style = doc.paragraphs[start].style

    for paragraph in list(doc.paragraphs[start : end + 1]):
        delete_paragraph(paragraph)

    current = anchor
    for step in PRACTICE_STEPS:
        current = insert_after(current, step, list_style)
    for marker in SCREENSHOT_MARKERS:
        current = insert_after(current, marker)

    doc.save(OUT)

    check = Document(OUT)
    text = "\n".join(p.text for p in check.paragraphs)
    print("saved", OUT)
    print("pictures", len(check.inline_shapes))
    print("screenshot_markers", text.count("[СКРИНШОТ:"))
    print("has_canva_url", "https://www.canva.com/" in text)
    print("has_signup", "Зарегистрироваться" in text)


if __name__ == "__main__":
    main()
